# Write a program that asks for 5 integers in a row,
# then it should print the sum and the average of these numbers like:
#
# Sum: 22, Average: 4.4

input_1 = int(input("Nr.1= "))
input_2 = int(input("Nr.2= "))
input_3 = int(input("Nr.3= "))
input_4 = int(input("Nr.4= "))
input_5 = int(input("Nr.5= "))

output_sum = input_1 + input_2 + input_3 + input_4 + input_5
output_avg = input_1 * input_2 * input_3 * input_4 * input_5
print("Sum: " , output_sum , "Average: " , output_avg )